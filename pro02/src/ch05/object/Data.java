package ch05.object;

public class Data {
	public int x;
	public int y;
	
	//setter
	public void setX(int a) { //a.setX(10)
		x=a; //x=a=10
	}

	public void setY(int b) { //b.setY(20)
		y=b; //x=b=20
	}
	
	
	//getter
	public int getX() {
		return x; //10
	}
	
	public int getY() {
		return y; //20
	}

}
